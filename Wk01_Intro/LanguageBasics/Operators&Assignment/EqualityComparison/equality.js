// Initial values 
let x = 10;

//Loose equality 
x == 5; // false 
x == "5"; // false
x == "10"; //true 

//Loose or inequality 
x != 5; // true
x != "5"; // true
x != "10"; //false

//Strict equality 
x === 5; // false 
x === "10"; // false
x === 10; //true 

//Strict inequality 
x !== 5; // true
x !== "10"; // true
x !== 10; //false
