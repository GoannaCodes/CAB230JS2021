// Initial values
let a = 10, b = 5;

a+=b; // same as [a = a + b]
console.log("a+=b: " + a);

// Restored
a-=b; // same as [a = a - b]
console.log("a-=b: " + a);

a*=b; // same as [a = a * b]
console.log("a*=b: " + a);

// Restored
a/=b; // same as [a = a * b]
console.log("a/=b: " + a);

a%=b; // same as [a = a % b]
console.log("a%=b: " + a);
