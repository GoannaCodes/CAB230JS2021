let a = 3 + 4; //addition 7
console.log("a: " + a);

let b = 7 - 4; //subtraction 3
console.log("b: " + b);

let c = b++; //post-increment 3
console.log("c: " + c);

let d = c--; //post-decrement 3
console.log("d: " + d);

let e = ++b; //pre-increment 5
console.log("e: " + e);

let f = --e; //pre-decrement 4
console.log("f: " + f);
